package autohubb.vinture.com.autohubb.VehicleModule;

public class ServicePackListPojo {
    public String servicePackName="";
    public String productId="";
    public String id="";
    public String name="";
    public String description="";
    public String categoryId="";
    public String typeId="";
    public String currentStock="";
    public String image="";
    public String price="";
    public String CategoryName = "";


    public ServicePackListPojo(String servicePackName, String productId, String id, String name, String description, String categoryId, String typeId, String currentStock, String image, String price, String categoryName) {

        this.servicePackName = servicePackName;
        this.productId = productId;
        this.id = id;
        this.name = name;
        this.description = description;
        this.categoryId = categoryId;
        this.typeId = typeId;
        this.price = price;
        this.currentStock = currentStock;
        this.image = image;
        this.price = price;
        this.CategoryName = categoryName;
    }







}
